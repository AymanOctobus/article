package com.octobus.article.model;

public class Points {
	
	private String likePoints;
	private String dislikePoints;
	private String pointEffectiveFrom;

	public String getLikePoints() {
		return likePoints;
	}
	public void setLikePoints(String likePoints) {
		this.likePoints = likePoints;
	}
	public String getDislikePoints() {
		return dislikePoints;
	}
	public void setDislikePoints(String dislikePoints) {
		this.dislikePoints = dislikePoints;
	}
	public String getPointEffectiveFrom() {
		return pointEffectiveFrom;
	}
	public void setPointEffectiveFrom(String pointEffectiveFrom) {
		this.pointEffectiveFrom = pointEffectiveFrom;
	}
	
	
	
	
}
